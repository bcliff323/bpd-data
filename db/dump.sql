CREATE TABLE IF NOT EXISTS incidents (
	id SERIAL PRIMARY KEY,
	incident_number VARCHAR(255),
  call_type VARCHAR(255),
  call_type_group VARCHAR(255),
  call_time DATE,
  street VARCHAR(255),
  call_origin VARCHAR(255),
  mental_health VARCHAR(50),
  drug_related VARCHAR(50),
  dv_related VARCHAR(50),
  alcohol_related VARCHAR(50),
  area VARCHAR(50),
  area_name VARCHAR(255),
  latitude VARCHAR(255),
  longitude VARCHAR(255),
  day_of_week VARCHAR(255),
  incident_hour VARCHAR(255),
  incident_priority VARCHAR(255),
  incident_month VARCHAR(15)
);

CREATE TABLE IF NOT EXISTS arrests (
	id SERIAL PRIMARY KEY,
  incident_number VARCHAR(255),
  arrest_date DATE,
  gender VARCHAR(255),
  race VARCHAR(255),
  age VARCHAR(255),
  charge VARCHAR(255),
  most_serious VARCHAR(50),
  ethnicity VARCHAR(255),
  felony VARCHAR(255),
  violent VARCHAR(255),
  category VARCHAR(255),
  arrest_type VARCHAR(255),
  addl_charge_1 VARCHAR(255),
  addl_charge_2 VARCHAR(255),
  addl_charge_3 VARCHAR(255),
  addl_charge_4 VARCHAR(255),
  addl_charge_5 VARCHAR(255),
  addl_charge_6 VARCHAR(255),
  addl_charge_7 VARCHAR(255),
  addl_charge_8 VARCHAR(255)
);